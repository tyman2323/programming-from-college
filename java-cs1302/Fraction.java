package lab10;

public class Fraction{
	   private int numerator;
	   private int denominator;
	   
	   public Fraction(int d, int dd){
	      this.numerator = d;
	      this.denominator = dd;
	   }
	   
	   public int dengetter(){

	      return denominator;
	   }
	   
	   public int numgetter(){

	      return numerator;
	   }
	}